package typesOfOperator;

public class ArithmeticOperator {

	public static void main(String[] args) {
		
		System.out.println("+operator");
		
		int a= 20;
		int b= 15;
		int c= 35;
		int d= a+b+c;
		
		System.out.println("addition of a,b,c is:"+d);
		
		System.out.println("-operator");
		
		int x= 35;
		int y= 10;
		int z= x-y;
		
		System.out.println("subtraction of x and y is:"+z);
		
		System.out.println("*operator");
		
		int g= 15;
		int h= 10;
		int i= g*h;
		
		System.out.println("multiplication of g and h is:"+i);
		
		System.out.println("/operator");
		
		int m= 120;
		int n= 5;
		int o= m/n;
		
		System.out.println("division of m and n is:"+o);
		
		System.out.println("%operator");
		
		int s= 28;
		int t= 7;
		int u= s%t;
		
		System.out.println("mod of s and t is:"+u);
		
		
	}
}
