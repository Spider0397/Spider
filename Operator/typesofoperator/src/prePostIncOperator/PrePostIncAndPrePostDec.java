package prePostIncOperator;

public class PrePostIncAndPrePostDec {

	public static void main(String[] args) {
		
		int Z= 40;
		System.out.println(Z++);
		System.out.println(Z);
		System.out.println(++Z);
		System.out.println(Z);
		System.out.println(Z--);
		System.out.println(Z);
		System.out.println(--Z);
		System.out.println(Z);
		
	}
}
